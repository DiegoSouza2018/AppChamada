    public TelaPrincipal(Object nomeCurso, Object nomeTurma,ArrayList<Aluno> dados) {
        initComponents();
        txtCurso.setText(nomeCurso.toString());
        txtTurma.setText(nomeTurma.toString());
        //Data atual
        Date data = new Date();
    
        //String que armazena a data atual
        String dataAtual;
        dataAtual = java.text.DateFormat.getDateInstance(DateFormat.MEDIUM).format(data);
    
        //Titulo das colunas da tabela
        String[] titulo = new String[3];
        titulo[0]="MATRICULA";
        titulo[1]="NOME";
        titulo[2]=dataAtual;
        
        //Dados dos alunos convertidos para a tabela
        Object[][] dadosChamada = new Object[dados.size()][3];
        
        int i, j;
        for(i=0; i<dados.size(); i++){
            for(j=0; j<3; j++){
                if(j==0){
                    dadosChamada[i][j] = dados.get(i).getMatricula();
                }
                else if(j==1){
                    dadosChamada[i][j] = dados.get(i).getNome();
                }
                else
                {
                    dadosChamada[i][j] = dados.get(i).isStatus();
                }
            }
        }
        
        TabelaDaChamada2 tblChamada = new TabelaDaChamada2(dadosChamada, titulo);
        tblInserida.setModel(tblChamada);
        tblInserida.setFont(new Font("Monospaced", Font.PLAIN, 15));
        MyCellRendererOutrosValores renderer = new MyCellRendererOutrosValores(Color.CYAN, Color.ORANGE);
        tblInserida.setDefaultRenderer(String.class, renderer);
        Toolkit tk = Toolkit.getDefaultToolkit();
        Dimension d = tk.getScreenSize();
        this.setSize((int) d.getWidth(), d.height-40);
    }

