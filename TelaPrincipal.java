    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Cria Lista de Alunos
        ArrayList<Aluno> saveARQ = new ArrayList<Aluno>();
        Aluno[] recebido = new Aluno[this.tblInserida.getRowCount()];
        Object dadosTabela;
        int i, j;
        
        for(i=0; i<this.tblInserida.getRowCount(); i++){
            recebido[i] = new Aluno();
        }
        
        for(i=0; i<this.tblInserida.getRowCount(); i++){
            for(j=0; j<3; j++){
                dadosTabela = this.tblInserida.getValueAt(i,j);
                switch (j) {
                    case 0:
                        recebido[i].setMatricula(dadosTabela.toString());
                        break;
                    case 1:
                        recebido[i].setNome(dadosTabela.toString());
                        break;
                    case 2:
                        if(dadosTabela.toString()=="true"){
                            recebido[i].setStatus(true);
                        }
                        else
                        {
                            recebido[i].setStatus(false);
                        }
                }
            }
        }
        for(i=0; i<this.tblInserida.getRowCount(); i++){
            saveARQ.add(i, recebido[i]);
        }
        
        int res=0;
        
        //Data atual
        Date data = new Date();
    
        //String que armazena a data atual
        String dataAtual;
        String data2;
        
        data2 = java.text.DateFormat.getDateInstance(DateFormat.MEDIUM).format(data);
        dataAtual = java.text.DateFormat.getDateInstance(DateFormat.LONG).format(data);
        try {
            CrunchifyCSVtoArrayList salvo = new CrunchifyCSVtoArrayList();
            res = salvo.salvarArquivo(saveARQ, dataAtual, this.txtCurso.getText(), this.txtTurma.getText(), data2);
        } catch (IOException ex) {
            Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(res==1)
        {
            System.exit(0);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

